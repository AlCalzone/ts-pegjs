/**
 * Produces a name based off `nameProposal` that is guaranteed to not be a key in `nameMap`.
 */
export function getUniqueName(
  nameProposal: string,
  nameMap: Map<string, string>,
  excludeReservedWords?: boolean
): string {
  const origProposal = nameProposal;
  let i = 1;
  while (nameMap.has(nameProposal) || (excludeReservedWords && RESERVED_WORDS.has(nameProposal))) {
    nameProposal = `${origProposal}_${i}`;
    i++;
  }
  return nameProposal;
}

/**
 * Checks if `name` is a Javascript or Typescript reserved word.
 */
export function isKeyword(name: string): boolean {
  return RESERVED_WORDS.has(name);
}

